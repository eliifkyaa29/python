kisakenar=int(input("Kısa kenarı giriniz:")) #Kullanıcıdan kısakenarı alınız.
uzunkenar=int(input("Uzun kenarı giriniz:")) #Kullanıcıdan uzunkenar alınız.
alan=kisakenar*uzunkenar                     #Alanı hesaplandı.
cevre=(kisakenar*uzunkenar)*2                #Dikdörtgenin çevresi hesaplandı.
print("Dikdörtgen Alanı:" , alan)
print("Dikdörtgenin Çevresi:", cevre)
